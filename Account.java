package javaapplication1;

public class Account
{ 
    private String Name ;
    public void setName(String name)
    {
        this.Name = name;
        
    }
    public String getName()
    {
        return Name;        
    }
    
    public int IdAccount;
    public void setIdAccount(int id)
    {
        this.IdAccount = id;
    }
    public int getAccount()
    { 
        return IdAccount;
    }
    
       
}

